cor = input("Qual a cor do semáforo?: ")

if cor == "vermelho" or "Vermelho":
  print("Sinal Vermelho... PARE")
  
elif cor == "amarelo" or "Amarelo":
    print("Sinal Amarelo... ATENÇÃO")
    
elif cor == "verde" or "Verde":
    print("Sinal Verde... SIGA")
    
else:
    print("Cor Inválida")